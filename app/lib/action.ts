import { z } from 'zod';
// zod: 타입스크립트의 유효성 검사 라이브러리
// 데이터 구조를 정의하고, 해당 구조에 맞는 데이터인지 여부를 검증할 수 있다.
// 타입 안전성을 제공하고, 간편하게 데이터를 검증할 수 있는 강력한 도구이다.

export type State = {
  errors?: {
    customerId?: string[];
    amount?: string[];
    status?: string[];
  };
  message?: string | null;
};

const FormSchema = z.object({
  id: z.string(),
  customerId: z.string({
    invalid_type_error: 'Please select a customer.', // 고객 필드가 비어있으면 오류 발생
  }),
  amount: z.coerce.number().gt(0, {
    message: 'Please enter an amount greater than $0.', // 0보다 큰 금액이 아닌 경우 오류 발생
  }), // coercion: 강제 변환
  status: z.enum(['pending', 'paid'], {
    invalid_type_error: 'Please select an invoice status.', // 값이 비어있으면 오류 발생
  }),
  date: z.string(),
});

// omit: 기존 스키마에서 특정 필드를 제외한 새로운 스키마를 생성
// 기존 스키마를 수정하지 않고, 필요한 필드만 선택적으로 포함하거나 제외한 새로운 스키마를 생성할 수 있다.
const CreateInvoice = FormSchema.omit({ id: true, date: true });
const UpdateInvoice = FormSchema.omit({ id: true, date: true });

